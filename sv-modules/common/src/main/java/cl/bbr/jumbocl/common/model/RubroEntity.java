package cl.bbr.jumbocl.common.model;

public class RubroEntity {
	
	private int idSeccion;
	private int idRubro;
	private String nombreRubro;
	
	
	
	/**
	 * @return el idSeccion
	 */
	public int getIdSeccion() {
		return idSeccion;
	}
	/**
	 * @param idSeccion el idSeccion a establecer
	 */
	public void setIdSeccion(int idSeccion) {
		this.idSeccion = idSeccion;
	}
	/**
	 * @return el idRubro
	 */
	public int getIdRubro() {
		return idRubro;
	}
	/**
	 * @param idRubro el idRubro a establecer
	 */
	public void setIdRubro(int idRubro) {
		this.idRubro = idRubro;
	}
	/**
	 * @return el nombreRubro
	 */
	public String getNombreRubro() {
		return nombreRubro;
	}
	/**
	 * @param nombreRubro el nombreRubro a establecer
	 */
	public void setNombreRubro(String nombreRubro) {
		this.nombreRubro = nombreRubro;
	}
	
	

}
