package cl.bbr.promo.lib.dto;

public class PromoMedioPagoDTO {
	private String mp_promo;
	private String mp_jmcl;
	private int mp_jmcl_ncuotas;
	
	
	/**
	 * @return Returns the mp_jmcl.
	 */
	public String getMp_jmcl() {
		return mp_jmcl;
	}
	/**
	 * @param mp_jmcl The mp_jmcl to set.
	 */
	public void setMp_jmcl(String mp_jmcl) {
		this.mp_jmcl = mp_jmcl;
	}
	/**
	 * @return Returns the mp_promo.
	 */
	public String getMp_promo() {
		return mp_promo;
	}
	/**
	 * @param mp_promo The mp_promo to set.
	 */
	public void setMp_promo(String mp_promo) {
		this.mp_promo = mp_promo;
	}
	/**
	 * @return Returns the mp_jmcl_ncuotas.
	 */
	public int getMp_jmcl_ncuotas() {
		return mp_jmcl_ncuotas;
	}
	/**
	 * @param mp_jmcl_ncuotas The mp_jmcl_ncuotas to set.
	 */
	public void setMp_jmcl_ncuotas(int mp_jmcl_ncuotas) {
		this.mp_jmcl_ncuotas = mp_jmcl_ncuotas;
	}
		
	
}
