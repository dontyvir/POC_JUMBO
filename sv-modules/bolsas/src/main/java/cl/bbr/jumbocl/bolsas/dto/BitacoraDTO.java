package cl.bbr.jumbocl.bolsas.dto;

/**
 * @author ctapiat
 *
 */
public class BitacoraDTO {
	
	String cod_operacion;
	String desc_operacion;
	String fecha_operacion;
	String usuario_operacion;
	String hora_operacion;

	/**
	 * @return Devuelve cod_operacion.
	 */
	public String getCod_operacion() {
		return cod_operacion;
	}
	/**
	 * @param cod_operacion El cod_operacion a establecer.
	 */
	public void setCod_operacion(String cod_operacion) {
		this.cod_operacion = cod_operacion;
	}
	/**
	 * @return Devuelve desc_operacion.
	 */
	public String getDesc_operacion() {
		return desc_operacion;
	}
	/**
	 * @param desc_operacion El desc_operacion a establecer.
	 */
	public void setDesc_operacion(String desc_operacion) {
		this.desc_operacion = desc_operacion;
	}
	/**
	 * @return Devuelve fecha_operacion.
	 */
	public String getFecha_operacion() {
		return fecha_operacion;
	}
	/**
	 * @param fecha_operacion El fecha_operacion a establecer.
	 */
	public void setFecha_operacion(String fecha_operacion) {
		this.fecha_operacion = fecha_operacion;
	}
	/**
	 * @return Devuelve usuario_operacion.
	 */
	public String getUsuario_operacion() {
		return usuario_operacion;
	}
	/**
	 * @param usuario_operacion El usuario_operacion a establecer.
	 */
	public void setUsuario_operacion(String usuario_operacion) {
		this.usuario_operacion = usuario_operacion;
	}
	/**
	 * @return Devuelve hora_operacion.
	 */
	public String getHora_operacion() {
		return hora_operacion;
	}
	/**
	 * @param hora_operacion El hora_operacion a establecer.
	 */
	public void setHora_operacion(String hora_operacion) {
		this.hora_operacion = hora_operacion;
	}
}
