package cl.bbr.jumbocl.contenidos.exceptions;

public class EstadosException extends java.lang.Exception {

	/**
	 * 
	 */
	public EstadosException() {
		super();
	}

	/**
	 * @param message
	 * @param cause
	 */
	public EstadosException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param message
	 */
	public EstadosException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public EstadosException(Throwable cause) {
		super(cause);
	}

}
