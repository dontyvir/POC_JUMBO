package cl.bbr.jumbocl.contenidos.exceptions;

public class ProductosSapException extends java.lang.Exception {

	/**
	 * 
	 */
	public ProductosSapException() {
		super();
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ProductosSapException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param message
	 */
	public ProductosSapException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public ProductosSapException(Throwable cause) {
		super(cause);
	}

}
