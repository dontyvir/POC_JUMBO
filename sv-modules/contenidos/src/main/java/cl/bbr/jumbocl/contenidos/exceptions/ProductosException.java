package cl.bbr.jumbocl.contenidos.exceptions;

public class ProductosException extends java.lang.Exception {

	/**
	 * 
	 */
	public ProductosException() {
		super();
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ProductosException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param message
	 */
	public ProductosException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public ProductosException(Throwable cause) {
		super(cause);
	}

}
